package ch01;

public class CharVariableExample {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = 65;
        char c3 = '\u0041';

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);

        // 문자열
        String str = ""; // 문자열 초기화
        String str2 = "값을 표현할 수 있습니다.";

        System.out.println(str2);
    }
}
